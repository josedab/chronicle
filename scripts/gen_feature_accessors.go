// gen_feature_accessors generates lazy-init accessor methods for FeatureManager.
//
// Usage:
//
//	go run scripts/gen_feature_accessors.go -out feature_manager_gen.go
//
// Each feature definition specifies:
//   - Method: exported accessor name (e.g., "CQLEngine")
//   - Type: return type (e.g., "CQLEngine")
//   - Field: struct field name (e.g., "cqlEngine")
//   - InitName: name passed to safeInit (e.g., "cql")
//   - Constructor: Go expression for initialization (e.g., "NewCQLEngine(fm.db, fm.cqlConfig)")
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"log"
	"os"
	"text/template"
)

type Feature struct {
	Method      string // Exported method name
	Type        string // Return pointer type
	Field       string // Struct field name
	InitName    string // Name for safeInit logging
	Constructor string // Constructor expression
}

// features defines all lazy-init features.
// Add new features here and run go generate to update the accessor methods.

var accessorTmpl = template.Must(template.New("accessor").Parse(`
// {{.Method}} returns the {{.Type}} instance (lazy-initialized).
func (fm *FeatureManager) {{.Method}}() *{{.Type}} {
	fm.{{.Field}}Once.Do(func() {
		safeInit("{{.InitName}}", func() {
			fm.{{.Field}} = {{.Constructor}}
		})
	})
	return fm.{{.Field}}
}
`))

func main() {
	out := flag.String("out", "", "output file (default: stdout)")
	flag.Parse()

	var buf bytes.Buffer
	fmt.Fprintf(&buf, "// Code generated by gen_feature_accessors.go; DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package chronicle\n\n")

	for _, f := range features {
		if err := accessorTmpl.Execute(&buf, f); err != nil {
			log.Fatalf("template: %v", err)
		}
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("gofmt: %v", err)
	}

	if *out != "" {
		if err := os.WriteFile(*out, src, 0644); err != nil {
			log.Fatalf("write: %v", err)
		}
		fmt.Printf("Generated %d accessor methods in %s\n", len(features), *out)
	} else {
		os.Stdout.Write(src)
	}
}
